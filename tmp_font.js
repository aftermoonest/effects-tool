const map = {};
for (const [k, rows] of Object.entries(chars)) {
    let intVal = 0;
    // rows go from top to bottom
    // columns from left to right
    for (let y = 0; y < 5; y++) {
        for (let x = 0; x < 5; x++) {
            let bit = x + 5 * y;
            // The shader reads: `mod(floor(float(n) / pow(2.0, float(24 - bit))), 2.0)`
            // This means bit 0 is the highest bit (pow 24)
            // bit 24 is the lowest bit (pow 0)
            if (rows[y][x] === '1') {
                intVal += Math.pow(2, 24 - bit);
            }
        }
    }
    map[k] = intVal;
}

